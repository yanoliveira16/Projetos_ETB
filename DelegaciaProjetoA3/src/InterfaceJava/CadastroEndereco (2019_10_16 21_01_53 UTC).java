     public boolean verificaDados(){
       if(!jTNumero.getText().equals("") && !jTRua.getText().equals("")){
        
       return true;
     }else{
            JOptionPane.showMessageDialog(null,"Campos N√∫mero ou Rua n√£o preenchidos" );
     return false;}
     }
     public void desabilitaDados(){
     jTNumero.setEditable(false);
     jTRua.setEditable(false);     
     }
     public void habilitaDados(){
     jTNumero.setEditable(true);
     jTRua.setEditable(true);
         
     }
     public void listarEnderecos(){
        try {
            Enderecos = EnderecoDAO.leTodosEnd("%"+jTPesquisa.getText()+"%");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "problema em listar"+ex);
        }
          mostraPesquisa(Enderecos);
     }
     public void mostraPesquisa(List<Endereco> Enderecos){
         while(tmContato.getRowCount()>0){
             tmContato.removeRow(0);
         }
         if(Enderecos.size() == 0){
             JOptionPane.showMessageDialog(null, "Nenhum Endere√ßo Cadastrado");
         }else{
             String[] linha = new String[]{null,null};
             for (int i = 0; i <Enderecos.size(); i++) {
                 tmContato.addRow(linha);
                 tmContato.setValueAt(Enderecos.get(i).getRua(), i, 0 );
                 tmContato.setValueAt(Enderecos.get(i).getNumero(), i, 1 );
             }
         }
     }
     
     private void jTTabelaLinhaSelecionada(JTable tabela) {
        if (jTTabela.getSelectedRow() != -1) {
            jTNumero.setText(String.valueOf(Enderecos.get(tabela.getSelectedRow()).getNumero()));
            jTRua.setText(Enderecos.get(tabela.getSelectedRow()).getRua());
        } else {
            jTNumero.setText("");
            jTRua.setText("");
        }
        
}    public void excluirEndereco(){
    int resp = JOptionPane.showConfirmDialog(this,"Deseja Realmente excluir esse endere√ßo?",
                "Confirma√ß√£o",JOptionPane.YES_NO_OPTION);
            if(resp == JOptionPane.YES_OPTION ){
                EnderecoDAO dao = new EnderecoDAO();
               try{
                dao.exclui(Enderecos.get(jTTabela.getSelectedRow()).getNumero());
                JOptionPane.showMessageDialog(null, "excluido com sucesso");
                mostraPesquisa(Enderecos);
               }catch(Exception ex){
                   
               }
            }else{
                JOptionPane.showMessageDialog(null, "Cancelado");
            }
}

   private void alteraEnderecoRua(){
       if(jTTabela.getSelectedRow() != -1){
           if(verificaDados()){
               Endereco end = new Endereco();
               end.setRua(jTRua.getText());
               end.setNumero(Integer.parseInt(jTNumero.getText()));
               try {
                   EnderecoDAO.alteraRua(end);
                   JOptionPane.showMessageDialog(null, "Alterado com sucesso");
               } catch (Exception ex) {
                  JOptionPane.showMessageDialog(null, "Erro em Alterar"+ex);
               }
           }
       }
   }
   
    private void alteraEnderecoNum(){
       if(jTTabela.getSelectedRow() != -1){
           if(verificaDados()){
               Endereco end = new Endereco();
               end.setNumero(Integer.parseInt(jTNumero.getText()));
               end.setRua(jTRua.getText());
               try {
                   EnderecoDAO.alteraNumero(end);
                   JOptionPane.showMessageDialog(null, "Alterado com sucesso");
               } catch (Exception ex) {
                  JOptionPane.showMessageDialog(null, "Erro em Alterar"+ex);
               }
           }
       }
   }

